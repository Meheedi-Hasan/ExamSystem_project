    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // Start Exam Button
        String course=jTextField1.getText();
        String query="select * from coursecode where  course='"+course+"'";
        try{
             
            
            
            pst=conn.prepareStatement(query);
            rs = pst.executeQuery();
            if(rs.next())
            {
                ExamPaper xp = new ExamPaper();
                xp.setVisible(true);
                this.setVisible(false);
            }
            else
            {
                JOptionPane.showMessageDialog(null, "wrong course code","Error",JOptionPane.ERROR_MESSAGE);
            }
            
        }catch(Exception e){
            
            System.out.println(e);
        }
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

