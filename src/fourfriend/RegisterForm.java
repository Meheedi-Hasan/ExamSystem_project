         String lname=jTextField3.getText();
         String email=jTextField4.getText();
         String contact_no=jTextField5.getText();
         String gender="Male";
         
         if(jRadioButton2.isSelected()){
             
             gender="Female";
         }
         
         
         String country=(String)jComboBox1.getSelectedItem();
         String address=jTextArea1.getText();
         
         String query ="select * from register_info where username ='"+username+"' and password = '"+password+"'";
        
        
        try{
         
            pst=conn.prepareStatement(query);
            
            rs=pst.executeQuery();
            
            if(rs.next())
            {
                System.out.println("Already Register");
            }
            else
            {
                String query1="insert into register_info values(null,'"+username+"','"+password+"','"+fname+"','"+mname+"','"+lname+"','"+gender+"','"+email+"','"+contact_no+"','"+country+"','"+address+"')";
                
                pst.executeUpdate(query1);
                Login ln = new Login();
                ln.setVisible(true);
                this.setVisible(false);
                
            }
            
        }
        catch(Exception e)
        {
            System.out.println(e);
        }
         

         
